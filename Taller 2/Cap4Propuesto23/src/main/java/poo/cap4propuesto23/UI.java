    private void btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActionPerformed
        if (solved) {
            // Set visible again

            txtA.setEditable(true);
            txtB.setEditable(true);
            txtC.setEditable(true);
            txtA.setVisible(true);
            txtB.setVisible(true);
            txtC.setVisible(true);
            lblA.setVisible(true);
            lblB.setVisible(true);
            lblC.setVisible(true);

            // reset text
            txtA.setText("");
            txtB.setText("");
            txtC.setText("");
            lblDescription.setText("Sea Ax^2 + Bx + C, Ingresa:");
            lblA.setText("A");
            lblB.setText("B");

            // reset button
            btn.setText("Continuar");
            solved = false;
        } else {
            // GET ALL THE INFO

            Ecuacion cuadratica = new Ecuacion();
            try {
                cuadratica.A = Double.parseDouble(txtA.getText());
                cuadratica.B = Double.parseDouble(txtB.getText());
                cuadratica.C = Double.parseDouble(txtC.getText());
                // SET ALL THE INFO
                lblDescription.setText("Sea " + cuadratica.A + "x^2 + " + cuadratica.B + "x + " + cuadratica.C + ". Soluci√≥n");
                lblA.setText("x1");
                lblB.setText("x2");

                txtA.setEditable(false);
                txtB.setEditable(false);
                txtC.setEditable(false);

                if (cuadratica.getDiscriminante() < 0) {
                    txtA.setText("INFINITAS SOLUCIONES");
                    txtB.setVisible(false);
                    lblB.setVisible(false);
                } else if (cuadratica.getDiscriminante() == 0) {
                    txtA.setText(String.valueOf(cuadratica.getOne()));
                    txtB.setVisible(false);
                    lblB.setVisible(false);

                } else {
                    txtA.setText(String.valueOf(cuadratica.getOne() + cuadratica.getSecondPart()));
                    txtB.setText(String.valueOf(cuadratica.getOne() - cuadratica.getSecondPart()));
                }

                // HIDE NOT USED
                txtC.setVisible(false);
                lblC.setVisible(false);

                // BUTTON CHANGE
                btn.setText("Limpiar");
                solved = true;
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null,
                        "Ten cuidado con los datos ingresados. Algun(os) no tienen el tipo de dato correcto.",
                        "Datos Incorrectos",
                        JOptionPane.ERROR_MESSAGE);
            }

        }


    }//GEN-LAST:event_btnActionPerformed

